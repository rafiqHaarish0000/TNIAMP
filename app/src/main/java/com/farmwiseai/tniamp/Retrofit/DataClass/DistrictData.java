package com.farmwiseai.tniamp.Retrofit.DataClass;

public class DistrictData {
    public int ID;
    public String NAME;
    public int SUB_BASIN_ID;
    public int REVISION_NO;

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getNAME() {
        return NAME;
    }

    public void setNAME(String NAME) {
        this.NAME = NAME;
    }

    public int getSUB_BASIN_ID() {
        return SUB_BASIN_ID;
    }

    public void setSUB_BASIN_ID(int SUB_BASIN_ID) {
        this.SUB_BASIN_ID = SUB_BASIN_ID;
    }

    public int getREVISION_NO() {
        return REVISION_NO;
    }

    public void setREVISION_NO(int REVISION_NO) {
        this.REVISION_NO = REVISION_NO;
    }
}
